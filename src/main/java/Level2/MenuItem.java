package Level2;


public class MenuItem {

    //필드 : 이름, 가격, 설명
    String name;
    String price;
    String description;


    //생성자
    public MenuItem (String _name, String _price, String _description) {
        this.name = _name;
        this.price = _price;
        this.description = _description;
    }
}
